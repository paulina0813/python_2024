'''
009. Escape Sequence
\n = new line
\t = tab
'''

weather = "it's sunny"
print(weather)

weather = 'it\'s sunny'
print(weather)

first_name = 'Paulina'
last_name = 'Remis'

print(first_name + '\n' + last_name)
print(first_name + '\t' + last_name)

print('First Name: ' + first_name + '\n' + 'Last Name: ' + last_name)

print(first_name)
print('\t' + last_name)