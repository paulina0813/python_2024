for item in range(10): #Prints from 0 to 9
    print(item)

for item in range(1,10): #Prints from 1 to 9
    print(item)

for item in range(3,8): #Prints from 3 to 9
    print(item)

for item in range(5, 16, 2): #(beginning, end, step)
    print(item)

for item in range(10, 0, -1):
    print(item)

