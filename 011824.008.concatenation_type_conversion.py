'''
008. Concatenation & Type Conversion
To concatenate is to combine two or more strings to obtain a new string
'''

first_name = 'Paulina'
last_name = 'Remis'

print(first_name +' '+ last_name)

full_name = first_name + ' ' + last_name
print(full_name)

year_born = 1995
print(full_name + ' ' + str(year_born))

current_year = 2024
print(year_born + current_year)
print(str(year_born) + ' ' + str(current_year))
print(str(year_born) + '\n' + str(current_year))