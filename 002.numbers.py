'''
002. Numbers
In Python there are two main types of numbers, integer and float, although sometimes we also use a type of number called "complex"
'''

print(type(2024)) #int - has no decimals
print(type(13.13)) #float - has decimals
print(type(5j)) #complex - have a 'j' to represent the imaginary part

print(2024)
print(13.13)
print(5j)